/**
 * Writes `public/firebase-messaging-sw.js` for FCM background messages.
 * When NEXT_PUBLIC_FIREBASE_* + VAPID are missing, writes a no-op worker.
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const outPath = path.join(root, "public", "firebase-messaging-sw.js");

function loadDotEnvFile(rel) {
  const p = path.join(root, rel);
  if (!fs.existsSync(p)) return {};
  const raw = fs.readFileSync(p, "utf8");
  const env = {};
  for (const line of raw.split(/\r?\n/)) {
    const m = line.match(/^\s*([A-Za-z_][\w]*)\s*=\s*(.*)$/);
    if (!m) continue;
    let v = m[2].trim();
    if (
      (v.startsWith('"') && v.endsWith('"')) ||
      (v.startsWith("'") && v.endsWith("'"))
    ) {
      v = v.slice(1, -1);
    }
    env[m[1]] = v;
  }
  return env;
}

const env = { ...process.env, ...loadDotEnvFile(".env.local"), ...loadDotEnvFile(".env") };

const required = [
  "NEXT_PUBLIC_FIREBASE_API_KEY",
  "NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN",
  "NEXT_PUBLIC_FIREBASE_PROJECT_ID",
  "NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET",
  "NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID",
  "NEXT_PUBLIC_FIREBASE_APP_ID",
  "NEXT_PUBLIC_FIREBASE_VAPID_KEY",
];

const missing = required.filter((k) => !String(env[k] ?? "").trim());

/** Keep aligned with `firebase` npm major for compat importScripts URLs. */
const FB_VERSION = "12.11.0";

const noopSw = `/* auto-generated — FCM disabled (missing env in gen-firebase-messaging-sw.mjs) */
self.addEventListener("install", (e) => self.skipWaiting());
self.addEventListener("activate", (e) => e.waitUntil(self.clients.claim()));
`;

if (missing.length) {
  fs.writeFileSync(outPath, noopSw, "utf8");
  console.warn(
    "[gen-firebase-messaging-sw] Skipping FCM worker config; missing:",
    missing.join(", "),
  );
  process.exit(0);
}

const firebaseConfig = {
  apiKey: env.NEXT_PUBLIC_FIREBASE_API_KEY.trim(),
  authDomain: env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN.trim(),
  projectId: env.NEXT_PUBLIC_FIREBASE_PROJECT_ID.trim(),
  storageBucket: env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET.trim(),
  messagingSenderId: env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID.trim(),
  appId: env.NEXT_PUBLIC_FIREBASE_APP_ID.trim(),
};

const configJson = JSON.stringify(firebaseConfig);

const sw = `/* auto-generated by scripts/gen-firebase-messaging-sw.mjs — do not edit */
/* eslint-disable no-undef */
importScripts("https://www.gstatic.com/firebasejs/${FB_VERSION}/firebase-app-compat.js");
importScripts("https://www.gstatic.com/firebasejs/${FB_VERSION}/firebase-messaging-compat.js");

firebase.initializeApp(${configJson});

const messaging = firebase.messaging();

messaging.onBackgroundMessage((payload) => {
  const title =
    payload.notification?.title || "Next";
  const body =
    payload.notification?.body || "";
  self.registration.showNotification(title, {
    body,
    icon: "/icon-192.png",
    data: payload.data || {},
  });
});

self.addEventListener("install", (e) => self.skipWaiting());
self.addEventListener("activate", (e) => e.waitUntil(self.clients.claim()));
`;

fs.writeFileSync(outPath, sw, "utf8");
console.log("[gen-firebase-messaging-sw] Wrote", path.relative(root, outPath));
